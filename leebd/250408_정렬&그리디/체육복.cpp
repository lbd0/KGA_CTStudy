/************체육복****************
문제 설명
점심시간에 도둑이 들어, 일부 학생이 체육복을 도난당했습니다. 
다행히 여벌 체육복이 있는 학생이 이들에게 체육복을 빌려주려 합니다. 
학생들의 번호는 체격 순으로 매겨져 있어, 바로 앞번호의 학생이나 바로 뒷번호의 학생에게만 체육복을 빌려줄 수 있습니다. 
예를 들어, 4번 학생은 3번 학생이나 5번 학생에게만 체육복을 빌려줄 수 있습니다. 
체육복이 없으면 수업을 들을 수 없기 때문에 체육복을 적절히 빌려 최대한 많은 학생이 체육수업을 들어야 합니다.

전체 학생의 수 n, 체육복을 도난당한 학생들의 번호가 담긴 배열 lost, 여벌의 체육복을 가져온 학생들의 번호가 담긴 배열 reserve가 매개변수로 주어질 때, 체육수업을 들을 수 있는 학생의 최댓값을 return 하도록 solution 함수를 작성해주세요.

제한사항
전체 학생의 수는 2명 이상 30명 이하입니다.
체육복을 도난당한 학생의 수는 1명 이상 n명 이하이고 중복되는 번호는 없습니다.
여벌의 체육복을 가져온 학생의 수는 1명 이상 n명 이하이고 중복되는 번호는 없습니다.
여벌 체육복이 있는 학생만 다른 학생에게 체육복을 빌려줄 수 있습니다.
여벌 체육복을 가져온 학생이 체육복을 도난당했을 수 있습니다. 이때 이 학생은 체육복을 하나만 도난당했다고 가정하며, 남은 체육복이 하나이기에 다른 학생에게는 체육복을 빌려줄 수 없습니다.
************************************/

#include <string>
#include <vector>

using namespace std;

int solution(int n, vector<int> lost, vector<int> reserve) {
    int answer = 0;
    
    vector<int> cloth;          // 모든 학생들의 체육복 수를 저장할 벡터
    
    for(int i=0; i<n; i++)
    {
        cloth.push_back(1);         // 도난 당하기 전 모두 1개씩 가지고 있다고 가정
    }
    
    for(auto r:reserve)     // 여분 챙겨온 학생들 적용
    {
        cloth[r-1] += 1;
    }
    
    for(auto l:lost)        // 도난 당한 학생들 적용
    {
        cloth[l-1] -= 1;
    }
    
    for(int i=0; i<n; i++)      // 체육복 빌려주기
    {
        if(cloth[i] == 0)       // 체육복이 0개 일 때
        {
            // 0번째 학생은 뒤에 학생만 고려, 마지막 학생은 앞에 학생만 고려
            if(i!=0 && cloth[i-1] == 2)     // 앞에 학생이 체육복이 2개면 빌리기
            {
                cloth[i] += 1;
                cloth[i-1] -= 1;
            }
            else if(i!=n-1 && cloth[i+1] == 2)  // 앞에 학생이 2개가 아니고, 뒤에 학생이 2개면 빌리기
            {
                cloth[i] += 1;
                cloth[i+1] -= 1;
            }
        }
    }
    
    for(auto c:cloth)       // 최종 체육복 벡터에서 0이 아니면 +1
    {
        if(c != 0) answer++;
    }
    
    return answer;
}